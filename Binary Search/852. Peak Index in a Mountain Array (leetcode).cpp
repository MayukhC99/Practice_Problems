class Solution {
public:
    
    int binary(vector<int>&ar ,int l, int r){
        while(l<=r){
            int mid = l + (r-l)/2;
            
            if(ar[mid]>ar[0] && ar[mid]>ar[mid-1])
                l = mid+1;
            else
                r = mid - 1;
        }
        return r;
    }
    
    int peakIndexInMountainArray(vector<int>& A) {
        return binary(A, 0 , A.size()-1);
    }
};


