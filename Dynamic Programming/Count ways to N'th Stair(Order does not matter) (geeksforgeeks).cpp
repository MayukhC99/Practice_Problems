// function to count ways to reach mth stair
long long dp[1000003]={0};
long long countWays(int m){
    if(m < 0)
        return 0;
    if(m == 0)
        return 1;
    if(dp[m] != 0)
        return dp[m];
    
    dp[m] = 1 + countWays(m-2) ;
    
    return dp[m];
}


