int dp[1000];

int solve(int i,vector<int>& ar){
    if(i >= ar.size())
        return 0;
    if(dp[i] != -1)
        return dp[i];
    return dp[i] = max(ar[i]+solve(i+2,ar) , solve(i+1,ar));
}

int houseRobber(std::vector<int> nums) {
    memset(dp , -1 , sizeof dp);
    return solve(0,nums);
}


