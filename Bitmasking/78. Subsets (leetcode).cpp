using namespace std;

class Solution {
public:
    vector<vector<int>> subsets(vector<int>& nums) {
        
        vector<vector<int>> ans;
        
        int n= nums.size();
        for(int i = 0 ; i < (1<<n) ; i++ ){
            
            int dig = i;
            vector<int> sub_ans;
            
            while(dig){
                int s= dig & (-dig);
                
                int pos= log2(s);
                sub_ans.push_back(nums[pos]);
                
                dig= dig - s;
            }
            
            ans.push_back(sub_ans);
        }
        
        
        return ans;
    }
};



