/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    int getDecimalValue(ListNode* head) {
        string s="";
        ListNode* q= head;
        
        while(q != NULL){
            s+= to_string(q->val);
            q= q->next;
        }
        
        bitset<32> base(s);
        
        return base.to_ulong();
    }
};


